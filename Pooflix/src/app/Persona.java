package app;

public class Persona {

    /***********
     * ATRIBUTOS
     ***********/
    private String nombre;
    private int edad;
    

    /******************
     * GETTERS Y SETTERS
     *******************/
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

}